    public void setMedico(Medico medico){
        this.medicoSelect = medico;
        txtMedico.setText(this.medicoSelect.getTexto());
        
        
        
        
        ComEspecialidad.removeAllItems();
        for(Especialidad espec: medico.getEspecialidades()){
            ComEspecialidad.addItem(espec.getNombre());
        }
        
        
        ComEspecialidad.setEnabled(true);
        
    }
    

