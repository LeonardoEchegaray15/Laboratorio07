    public void guardarCita(){
       
        
        if(medicoSelect != null && pacienteSelect != null){
             especialidadSelect = medicoSelect.getEspecialidades().get(ComEspecialidad.getSelectedIndex());
             
             cita = new Cita(medicoSelect, pacienteSelect,especialidadSelect);
             cita.setFecha(ComFecha.getSelectedIndex());
             cita.setHora(ComHora.getSelectedIndex());
             this.padre.AgregarCita(cita);
             
             
             //Si existe un medico y un paciente
             //entonces
             //voy a obtener la especialidadd selecionada
             //y luego agrego la cita
        }
            
        

    }
    
    
